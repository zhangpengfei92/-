package com.jcl.vo;

public class Msm {

	private String phone;
	
	private String code;
	
	private Long time;

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public Long getTime() {
		return time;
	}

	public void setTime(Long time) {
		this.time = time;
	}

	@Override
	public String toString() {
		return "Msm [phone=" + phone + ", code=" + code + ", time=" + time + "]";
	}
	
	
	
}
